/**
 * Expo config plugin: force the Android 12+ splash window and main
 * theme backgrounds to a brand color, overriding whatever expo-splash-screen
 * generated. Needed because some OEMs (MIUI/HyperOS) do not propagate the
 * splash-screen plugin's backgroundColor across the entire splash window —
 * only the top bar — leaving black edges around the icon. Writing these
 * attributes directly into styles.xml locks in the color OEM-wide.
 */
const {
  withAndroidColors,
  withAndroidStyles,
  AndroidConfig,
} = require('@expo/config-plugins');

const GREEN = '#0f2e24';
const COLOR_NAME = 'khanqahSplashBg';

function upsertStyleItem(theme, name, value) {
  theme.item = theme.item || [];
  const existing = theme.item.find((i) => i.$.name === name);
  if (existing) existing._ = value;
  else theme.item.push({ $: { name }, _: value });
}

const withSplashWindowBg = (config) => {
  config = withAndroidColors(config, (cfg) => {
    cfg.modResults = AndroidConfig.Colors.setColorItem(
      { $: { name: COLOR_NAME }, _: GREEN },
      cfg.modResults,
    );
    return cfg;
  });

  config = withAndroidStyles(config, (cfg) => {
    const styles = cfg.modResults;
    const ref = `@color/${COLOR_NAME}`;

    const allStyles = styles.resources.style || [];

    for (const theme of allStyles) {
      const name = theme.$.name;
      if (!name) continue;

      // Main activity theme — sets the window the ReactActivity inherits
      // before the JS surface mounts.
      if (name === 'AppTheme') {
        upsertStyleItem(theme, 'android:windowBackground', ref);
      }

      // Splash-screen theme generated by expo-splash-screen. Force all
      // three splash attributes so MIUI can't fall back to the system
      // default window background.
      if (name.includes('SplashScreen') || name.includes('Splash')) {
        upsertStyleItem(theme, 'android:windowBackground', ref);
        upsertStyleItem(theme, 'android:windowSplashScreenBackground', ref);
        upsertStyleItem(theme, 'android:windowSplashScreenIconBackgroundColor', ref);
      }
    }

    return cfg;
  });

  return config;
};

module.exports = withSplashWindowBg;
